def getMSCIFromLocal(index_name):
    """
    Loads MSCI index constituents from a local CSV file.
    This is the fastest and most reliable method.

    Parameters:
        index_name (str): Name of the MSCI index (e.g., "WORLD", "ACWI", "EUROPE")

    Expected file: MSCI_{index_name}.csv
    Expected format: CSV with ticker symbols in the first column

    Returns: List of tickers from the local CSV file
    """
    try:
        # Load from local CSV file
        csv_file = f"MSCI_{index_name}.csv"
        df = pd.read_csv(csv_file)

        # Extract ticker symbols from the first column
        ticker_column = df.columns[0]
        tickers = df[ticker_column].dropna().tolist()

        # Clean tickers (remove any non-alphanumeric characters except dots)
        cleaned_tickers = []
        for ticker in tickers:
            if isinstance(ticker, str):
                # Remove common suffixes and clean the ticker
                clean_ticker = ticker.strip().split(".")[0]  # Remove .L, .AS, etc.
                if clean_ticker and len(clean_ticker) <= 5:  # Reasonable ticker length
                    cleaned_tickers.append(clean_ticker)

        return cleaned_tickers

    except FileNotFoundError:
        print(f"Error: MSCI_{index_name}.csv file not found in current directory")
        print(
            f"Please download the MSCI {index_name} constituents CSV file and save it as 'MSCI_{index_name}.csv'"
        )
        return []
    except Exception as e:
        print(f"Error loading from local CSV: {e}")
        return []


def getMSCIWorldFromLocal():
    """
    Loads MSCI World constituents from a local CSV file.
    This is the fastest and most reliable method.

    Expected file: MSCI_WORLD.csv
    Expected format: CSV with ticker symbols in the first column

    Returns: List of tickers from the local CSV file
    """
    return getMSCIFromLocal("WORLD")


